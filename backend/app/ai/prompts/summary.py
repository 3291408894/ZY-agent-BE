DETAILED_SUMMARY_TEMPLATE = """请对以下课文内容进行详细的结构化总结，输出包含以下五个部分：

## 📖 全文主旨
用 2-3 句话概括文章的核心内容、中心思想和情感基调。

## 📑 段落精析
按段落顺序详细分析每段内容，包括：
- 段落大意
- 关键语句品读
- 在全文中的作用

## 🎯 核心考点
列出本文涉及的考试重点，按类别整理：
- **字词积累**：重点字音、字形、词语释义
- **文言知识**（如适用）：通假字、古今异义、词类活用、特殊句式
- **名句默写**：常考古诗文名句
- **文学常识**：作者、朝代、体裁、写作背景

## ✍️ 写作手法
分析文章使用的写作技巧和艺术特色：
- 修辞手法（比喻、拟人、排比等）
- 表现手法（借景抒情、托物言志、对比衬托等）
- 结构特点（总分总、倒叙、插叙等）

## 💡 学习启示
结合文章内容，给出学习建议和人生启示。

---

### 课文内容：
{content}

---

请使用 Markdown 格式输出详细总结，各部分使用二级标题（##）分隔。"""

